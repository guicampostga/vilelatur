package br.com.gfc.viagem;


public class ComplementoId {
	private String clienteNome;
	private String destino;
	private String origem;
	private String motoristaNome;
	private String veiculoNome;
	
	public String getClienteNome() {
		return clienteNome;
	}
	public void setClienteNome(String clienteNome) {
		this.clienteNome = clienteNome;
	}
	public String getDestino() {
		return destino;
	}
	public void setDestino(String destino) {
		this.destino = destino;
	}
	public String getOrigem() {
		return origem;
	}
	public void setOrigem(String origem) {
		this.origem = origem;
	}
	public String getMotoristaNome() {
		return motoristaNome;
	}
	public void setMotoristaNome(String motoristaNome) {
		this.motoristaNome = motoristaNome;
	}
	public String getVeiculoNome() {
		return veiculoNome;
	}
	public void setVeiculoNome(String veiculoNome) {
		this.veiculoNome = veiculoNome;
	}
}
